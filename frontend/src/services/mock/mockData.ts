/**
 * Helper function to generate realistic mock notes for any piece
 */
export function generateMockNotes(count: number = 20): Note[] {
  const pitches = ['C4', 'D4', 'E4', 'F4', 'G4', 'A4', 'B4'];
  const strings: Array<'G' | 'D' | 'A' | 'E'> = ['G', 'D', 'A', 'E'];
  const durations: Array<'whole' | 'half' | 'quarter' | 'eighth'> = ['quarter', 'quarter', 'eighth', 'half'];

  return Array.from({ length: count }, (_, i) => ({
    id: `generated-note-${i}`,
    sequenceNumber: i,
    pitch: pitches[i % pitches.length],
    octave: 4,
    duration: durations[i % durations.length],
    durationBeats: durations[i % durations.length] === 'quarter' ? 1.0 :
                   durations[i % durations.length] === 'eighth' ? 0.5 :
                   durations[i % durations.length] === 'half' ? 2.0 : 4.0,
    measure: Math.floor(i / 4) + 1,
    beat: (i % 4) + 1,
    positionInMeasure: i % 4,
    violinString: strings[i % strings.length],
    fingerPosition: (i % 4) + 1,
    handPosition: 1,
    bowDirection: i % 2 === 0 ? 'DOWN' : 'UP',
    bowPortion: 'MIDDLE',
    technique: 'DETACHE',
  }));
}
